package com.orionsolwings.osbiz.employee.management.model;

public class WorkExperience {
	private int totalExperienceInYears;
	private String previousCompany;
	private String lastDesignation;
	private String lastDrawnSalary;

	public int getTotalExperienceInYears() {
		return totalExperienceInYears;
	}

	public void setTotalExperienceInYears(int totalExperienceInYears) {
		this.totalExperienceInYears = totalExperienceInYears;
	}

	public String getPreviousCompany() {
		return previousCompany;
	}

	public void setPreviousCompany(String previousCompany) {
		this.previousCompany = previousCompany;
	}

	public String getLastDesignation() {
		return lastDesignation;
	}

	public void setLastDesignation(String lastDesignation) {
		this.lastDesignation = lastDesignation;
	}

	public String getLastDrawnSalary() {
		return lastDrawnSalary;
	}

	public void setLastDrawnSalary(String lastDrawnSalary) {
		this.lastDrawnSalary = lastDrawnSalary;
	}

	// Getters and Setters
}
